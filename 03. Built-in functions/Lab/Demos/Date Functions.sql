--DATEPART – extract a segment from a date as an integer
--Part can be any part and format of date or time
--DATEPART(Part, Date)
SELECT DATEPART(DAY, '12-31-2018 23:59:59')
SELECT DATEPART(MONTH, '12-31-2018 23:59:59')
SELECT DATEPART(YEAR, '12-31-2018 23:59:59')
SELECT DATEPART(WEEK, '12-31-2018 23:59:59')
SELECT DATEPART(WEEKDAY, '12-31-2018 23:59:59')
SELECT DATEPART(DAYOFYEAR, '12-31-2018 23:59:59')
SELECT DATEPART(HOUR, '12-31-2018 23:59:59')
SELECT DATEPART(MINUTE, '12-31-2018 23:59:59')
SELECT DATEPART(SECOND, '12-31-2018 23:59:59')
SELECT DATEPART(QUARTER, '12-31-2018 23:59:59')
	GO

--DATEDIFF – find difference between two dates
--Part can be any part and format of date or time
--DATEDIFF(Part, FirstDate, SecondDate)
SELECT DATEDIFF(YEAR, '12-31-2018', GETDATE())
SELECT DATEDIFF(MONTH, '12-31-2018', GETDATE())
SELECT DATEDIFF(DAY, '12-31-2018', GETDATE())
SELECT DATEDIFF(QUARTER, '12-31-2018', GETDATE())
SELECT DATEDIFF(HOUR, '12-31-2018', GETDATE())
SELECT DATEDIFF(MINUTE, '12-31-2018', GETDATE())
SELECT DATEDIFF(SECOND, '12-31-2018', GETDATE())
SELECT DATEDIFF(WEEK, '12-31-2018', GETDATE())
	GO

