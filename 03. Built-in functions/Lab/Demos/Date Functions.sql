  FROM Employees
	GO

--DATENAME – get a string representation of a date's part
--DATENAME(Part, Date)
SELECT DATENAME(WEEKDAY, '2017/01/27')
SELECT DATENAME(MONTH, '2017/01/27')
	GO

--DATEADD – perform date arithmetic
--Part can be any part and format of date or time
--DATEADD(Part, Number, Date)
SELECT DATEADD(DAY, 1, '2017/01/27')
SELECT DATEADD(DAY, 5, '2017/01/27')
SELECT DATEADD(MONTH, 11, '2017/01/27')
SELECT DATEADD(MONTH, 12, '2017/01/27')
SELECT DATEADD(YEAR, 1, '2017/01/27')
SELECT DATEADD(YEAR, -10, '2017/01/27')
	GO

